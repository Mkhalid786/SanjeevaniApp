    private boolean validateInputs() {
        loginId = txtUserId.getText().trim();
        char[] pwd = txtPassword.getPassword();
        if (loginId.isEmpty() || pwd.length == 0) {
            return false;
        }
        password = new String(pwd);
        return true;
    }

    private String getUsertype() {
        if (jrbAdmin.isSelected()) {
            return jrbAdmin.getText();
        } else if (jrbDocter.isSelected()) {
            return jrbDocter.getText();
        } else if (jrbReception.isSelected()) {
            return jrbReception.getText();
        } else {
            return null;
        }
    }
