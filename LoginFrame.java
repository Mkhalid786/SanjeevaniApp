    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        boolean isInputValid = validateInputs();
        if (isInputValid == false) {
            JOptionPane.showMessageDialog(null, "UserId or Password cannot be left empty", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String userType = getUsertype();
        if (userType == null) {
            JOptionPane.showMessageDialog(null, "Plz choose User Type", "Error!", JOptionPane.ERROR_MESSAGE);
            return;

        }
        try {
            User user = new User();
           // System.out.println(user);
            user.setLoginId(loginId);
            //System.out.println(password);
            String pwd = new String(PasswordEncryption.getEncryptedPassword(password));
            //System.out.println(pwd);
            user.setPassword(pwd);
            user.setUserType(userType);
            
            //System.out.println("before dao");
            String userName = UserDao.validateUser(user);
           //                                                                                                                                                                                                                                                                                                                                                                                                                         System.out.println("After dao");

            if (userName != null) {
                JOptionPane.showMessageDialog(null, "Login Accepted", "Welcome" + userName, JOptionPane.INFORMATION_MESSAGE);
                UserProfile.setUserName(userName);
                UserProfile.setUserType(userType);
                UserProfile.setUserId(loginId);
                

                if (userType.equalsIgnoreCase("admin")) {
                    AdminOptionFrame adminOptionFrame = new AdminOptionFrame();
                    adminOptionFrame.setVisible(true);
                    this.dispose();

                } else if (userType.equalsIgnoreCase("receptionist")) {
                    ReeptionistOptionFarme r = new ReeptionistOptionFarme();
                    r.setVisible(true);
                    this.dispose();

                } else {
                    DoctorOptionFrame a = new DoctorOptionFrame();
                    a.setVisible(true);
                    this.dispose();

                }

            } else {
                JOptionPane.showMessageDialog(null, "Invalid UserId/Password", "Login Denied", JOptionPane.ERROR_MESSAGE);
                
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "No UserId/Password found! Try Again" + ex, "Exception", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

